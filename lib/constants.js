const ELEVENLABS_API_KEY = process.env.NEXT_PUBLIC_ELEVENLABS_API_KEY


export async function fetchVoices() {
  const res = await fetch('https://api.elevenlabs.io/v1/voices', {
    headers: {
      'xi-api-key': ELEVENLABS_API_KEY,
      'Content-Type': 'application/json',
    },
  })

  if (!res.ok) {
    throw new Error('Failed to fetch ElevenLabs voices')
  }

  const data = await res.json()

  return data.voices
 
    .map((voice) => {
      const labels = voice.labels || {}

      return {
        id: voice.voice_id,
        name: buildVoiceName(voice.name, labels),
        gender: capitalize(labels.gender),
        accent: capitalize(labels.accent),
        elevenLabsId: voice.voice_id,
        previewUrl: voice.preview_url,
      }
    })
}

function buildVoiceName(name, labels) {
  // Add description if needed
  // For example: "Rachel - Professional"
  return name
}

function capitalize(str) {
  if (!str) return 'Unknown'
  return str.charAt(0).toUpperCase() + str.slice(1).toLowerCase()
}

// Export VOICES as a promise that resolves to the array
export const VOICES = fetchVoices()

// You can also export a function to get voices synchronously after they're loaded
let cachedVoices = null

export async function getVoices() {
  if (!cachedVoices) {
    cachedVoices = await fetchVoices()
  }
  return cachedVoices
}

  
